unsigned int step_map[MAZESIZE_X][MAZESIZE_Y];		// 歩数マップ

/*------------------------------------------------------------------------------*/
/* Function Definitions								*/
/*------------------------------------------------------------------------------*/

/*==============================================================================*/
/* 歩数マップの作成								*/
/* 										*/
/* 座標X,Yをゴールとした歩数マップを作成する					*/
/* ---------------------------------------------------------------------------- */
/*  入力:		wall_info		マップ壁情報			*/
/*  出力:		step_map		歩数マップ			*/
/*==============================================================================*/
void calc_map(void)
{
	int x;		// X座標
	int y;		// Y座標

	// 歩数マップを未到達扱いの255で初期化
	for(x = 0; x < MAZESIZE_X; x++)
	{
		for(y = 0; y < MAZESIZE_Y; y++)
		{
			step_map[x][y] = 255;
		}
	}

	// ゴール座標の歩数を0に設定
	step_map[GOAL_X][GOAL_Y] = 0;

	return;
}

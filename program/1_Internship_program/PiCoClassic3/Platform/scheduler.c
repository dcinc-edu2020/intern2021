/*==============================================================================*/
/* 定期処理									*/
/* 										*/
/* ソフトウェアのタスクを実行する						*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		-							*/
/*  Return   :		-							*/
/*==============================================================================*/
void task_routine(void)
{
	/* 初期化 */
	init_recognition();
	init_judgement();
	init_control();

	/* 1区画ごとに壁の有無を判定しながら進む */
	while(1)
	{
		/* 認知 */
		recognition();

		/* 判断 */
		judgement();

		/* 制御 */
		control();
	}

	return;
}
#include "common.h"

/*------------------------------------------------------------------------------*/
/* Global Variable								*/
/*------------------------------------------------------------------------------*/
volatile unsigned int timer;	// プログラム本体内のタイマー

/*------------------------------------------------------------------------------*/
/* Function Definitions								*/
/*------------------------------------------------------------------------------*/

/*==============================================================================*/
/* 待ち時間生成						                        */
/* 										*/
/* mS単位で待ち時間を生成する							*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		wate_time	待ち時間 [ms]				*/
/*  Return   :         	-							*/
/*==============================================================================*/
void wait_ms(int wate_time)
{
	unsigned int start_time;
	
	start_time = timer;
	
	while( (timer - start_time) < wate_time);
	
	return;
}
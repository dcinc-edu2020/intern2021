/*==============================================================================*/
/* モード表示(LED)					                        */
/* 										*/
/* 現在のモードを車体前方のLEDに表示する					*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		led_mode	1〜15までのモード情報			*/
/*  Return   :         	-							*/
/*==============================================================================*/
void display_led(int led_mode)
{
	LED0 = led_mode & 0x01;
	LED1 = (led_mode >> 1) & 0x01;
	LED2 = (led_mode >> 2) & 0x01;
	LED3 = (led_mode >> 3) & 0x01;
	
	return;
}
/* ソフトウェアのタスクを実行する						*/
/* ---------------------------------------------------------------------------- */
/*  Arguments:		-							*/
/*  Return   :         	-							*/
/*==============================================================================*/
void task_routine(void)
{
	/* まずは、スタート位置から0.5区画進む */
	run_straight(HALF_SECTION, DEFAULT_SPEED);
	
	/* 以降、1区画ごとに壁の有無を判定しながら空いている方向へ進む */
	while(1)
	{
		/* 認知 */
		recognition();
		
		/* 判断 */
		judgement();
		
		/* 制御 */
		control();
	}
	
	return;
}